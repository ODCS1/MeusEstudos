n1 = int(input('Digite o 1° número: '))
n2 = int(input('Digite o 2° número: '))
n3 = int(input('Digite o 3° número: '))

so = n1 + n2 + n3
su = n1 - n2 - n3
m = n1 * n2 * n3
d = n1 / n2 / n3
e = n1 ** n2 ** n3

print('Soma: {};\nSubtração: {};\nMultiplicação: {};\nDivisão: {};\nExpoente: {}.'.format(so,su,m,d,e))