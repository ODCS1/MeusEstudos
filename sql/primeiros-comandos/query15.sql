-- TIPOS DE JOINS


-- INNER JOIN

-- INNER JOIN retorna apenas os resultados que correspondem (existem) tanto na tabela A como na tabela B.

-- FULL OUTER JOIN

-- FULL OUTER JOIN retorna um conjunto de todos os registros correspondentes da TabelaA e TabelaB quando sãoiguais. E além disso senão houver valores correspondentes, ele simplismente irá preencher esselado com "null".

-- LEFT OUTER JOIN

-- LEFT OUTER JOIN retorna um conjunto de todos os registros da TabelaA, e além disso, os registros correspondentes(quando disponíveis) na TabelaB. Se não houver registros correspondentes ele simplesmente vai preencher com null.