-- CHAVE PRIMÁRIA

* Uma chave Primária é basicamente uma coluna ou grupo de colunas, usada para identificar unicamente uma linha em uma tabela.

* Você consegue criar essas chaves primárias atráves de restrições (ou constraint), que são regras que você define quando está criando uma coluna.

* Assim quando você faz isso você está criando um índice único para aquela coluna ou grupo de colunas.

-- CHAVE ESTRANGEIRA

* Uma chave estrangeira é uma coluna ou um grupo de colunas em uma tabela que indentifica unicamente uma linha em outra tabela.

* Ou seja, uma chave estrangeira é definida em uma tabela onde ela é apenas uma referência e não contém todos os dados ali.

* Então uma chave estrangeira é simplesmente uma conula ou grupo de colunas que é uma chave primária em outra tabela.